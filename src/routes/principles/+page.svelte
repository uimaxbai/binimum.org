        <div class="principle-number">01</div>
        <h2>Identify True Necessities</h2>
        <p>
          The first step in the Binimum journey is learning to distinguish between genuine needs and conditioned wants. This principle teaches us to recognize what truly supports our well-being versus what we've been taught to desire through marketing, social pressure, or habit.
        </p>
        <p>
          By honestly evaluating what serves an essential purpose in our lives, we create a foundation for meaningful simplicity.
        </p>
      </div>
      
      <div class="principle-card">
        <div class="principle-number">02</div>
        <h2>Embrace Quality Over Quantity</h2>
        <p>
          Binimum isn't about deprivation—it's about curation. This principle encourages us to choose fewer but better things that will serve us well and last longer.
        </p>
        <p>
          When we invest in quality for the few things we truly need, we reduce waste, save money long-term, and develop a deeper appreciation for what we have.
        </p>
      </div>
      
      <div class="principle-card">
        <div class="principle-number">03</div>
        <h2>Practice Intentional Consumption</h2>
        <p>
          This principle guides us to be mindful about what we bring into our lives—whether physical items, media, or commitments. Before acquiring something new, we pause to consider its true value and cost (not just financial, but in terms of time, attention, and space).
        </p>
        <p>
          Intentional consumption means making conscious choices rather than acting on impulse or autopilot.
        </p>
      </div>
      
      <div class="principle-card">
        <div class="principle-number">04</div>
        <h2>Create Space for What Matters</h2>
        <p>
          The purpose of reducing the unnecessary is to make room for the essential. This principle reminds us that simplifying isn't an end in itself, but a means to create space—physical, mental, and temporal—for what brings us genuine fulfillment.
        </p>
        <p>
          By clearing away excess, we open ourselves to deeper relationships, meaningful work, personal growth, and spiritual connection.
        </p>
      </div>
      
      <div class="principle-card">
        <div class="principle-number">05</div>
        <h2>Find Your Personal Minimum</h2>
        <p>
          The "bare minimum" looks different for everyone. This principle encourages us to discover our own unique balance point—where we have enough to thrive, but not so much that we're burdened.
        </p>
        <p>
          Your personal minimum will evolve with different life stages and circumstances, requiring ongoing reflection and adjustment.
        </p>
      </div>
    </div>
